package com.example.foodfind;

public class Recipe {
    private int recipeId;
    private int user;
    private String name;
    private String description;
    private String image;

    public int getRecipeId() {
        return recipeId;
    }

    public int getUser() {
        return user;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getImage() {
        return image;
    }
}
